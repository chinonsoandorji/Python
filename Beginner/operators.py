'''
This program performs arithmetic operations with
the different types of Operators in python
'''
# 1. The Exponential Operator
print(2 ** 3)

# 2. The Division Operator
print(100 / 5)

# 3. The Floor Divisin Operator  (it removes the decimal part from the answer)
print(10 // 3)

# 4. The Modulus Operator
print(20 % 4)
print(20 % 3) # result will give the remainder

# 5. The Addition Operator
print( 100 + 35)

# 6. The Minus Operator
print(30 - 23)